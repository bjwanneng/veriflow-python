# ─── Stimulus injection tracking ──────────────────────────────────────────
# Codegen MUST call mark_stimulus_injected() after calling drive_inputs() in
# test functions. This fail-fast check prevents false positives when codegen
# forgets to add stimulus (all DUT outputs stay at reset values, which may
# coincidentally match golden model trace).
#
# Uses a closure-based approach (instead of module-level mutable global) to
# remain correct even when cocotb runs tests in isolated subprocesses.

def _create_stimulus_tracker():
    """Create a stimulus tracker for a single test function.

    Returns (check, mark) where:
        check()  — raises AssertionError if no stimulus was injected
        mark()   — call after drive_inputs() to record stimulus was driven
    """
    _injected = [False]
    def mark():
        _injected[0] = True
    def check():
        if not _injected[0]:
            raise AssertionError(
                "No stimulus was driven to DUT — codegen MUST call "
                "drive_inputs() and mark_stimulus_injected() before "
                "the comparison loop. All DUT outputs are at reset values."
            )
    return check, mark

