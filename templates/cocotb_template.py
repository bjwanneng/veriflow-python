def _check_alignment(expected_cycles, dut_samples, cycle_offset=0, min_check_cycles=4):
    """Verify golden-to-RTL cycle alignment before the full comparison loop.

    Compares the first *min_check_cycles* golden entries against RTL samples.
    If ALL sampled values mismatch (highly unlikely for a true logic bug),
    the alignment offset (DRIVE_PHASE_CYCLES or golden cycle skip) is likely
    wrong. Returns (is_aligned, detail_string).
    """
    compare_start = 1 + cycle_offset  # skip reset cycle + offset
    available = min(len(expected_cycles) - compare_start, len(dut_samples), min_check_cycles)

    if available < 2:
        return True, ""  # not enough data to check

    total_checks = 0
    total_matches = 0

    for i in range(available):
        expected = expected_cycles[compare_start + i]
        sample = dut_samples[i]
        for sig_name, expected_val in expected.items():
            if sig_name in sample:
                total_checks += 1
                if sample[sig_name] == expected_val:
                    total_matches += 1

    if total_checks == 0:
        return True, ""

    match_rate = total_matches / total_checks

    if match_rate == 0:
        return False, (
            f"[ALIGN] ZERO matches in first {available} cycles "
            f"({total_checks} signal checks). "
            f"DRIVE_PHASE_CYCLES={cycle_offset} is likely wrong — "
            f"golden and RTL traces are misaligned. "
            f"Check the offset and re-run."
        )
    elif match_rate < 0.3:
        return False, (
            f"[ALIGN] Low match rate {match_rate:.0%} in first {available} cycles "
            f"({total_matches}/{total_checks}). "
            f"Possible partial alignment issue — verify DRIVE_PHASE_CYCLES={cycle_offset}."
        )

    return True, ""


# ─── Clock management ───────────────────────────────────────────────────
# CRITICAL: In cocotb v2.0+, background tasks (including Clock) are killed
# between tests. Each test MUST call ensure_clock() to start a fresh clock.

async def ensure_clock(dut):
    """Start clock. Must be called at the start of every test."""
    clock = Clock(dut.clk, CLK_PERIOD_NS, unit="ns")
    cocotb.start_soon(clock.start())
    await RisingEdge(dut.clk)


# ─── Test result tracking ──────────────────────────────────────────────
# Per-test failure tracking (avoids global state issues with cocotb v2.0+).
# Each test uses its own local counter; test_summary reads all test results.

def _make_fail_counter():
    """Create a per-test fail counter with increment and check methods."""
    count = 0

    class FailCounter:
        def inc(self):
            nonlocal count
            count += 1

        @property
        def value(self):
            return count

        @property
        def has_failures(self):
            return count > 0

    return FailCounter()


# ─── Helpers ────────────────────────────────────────────────────────────

async def reset_dut(dut, fail_counter=None):
    """Apply synchronous reset and wait for outputs to settle."""
    dut.rst.value = 1
    # Zero all input ports only (not outputs — outputs are driven by DUT)
    for name in INPUT_PORTS:
        try:
            sig = getattr(dut, name)
            if hasattr(sig, 'value') and hasattr(sig, 'setimmediatevalue'):
                sig.value = 0
        except Exception:
            pass
    # Also zero any valid input ports from HANDSHAKE_PORTS
    for valid_name in HANDSHAKE_PORTS:
        try:
            getattr(dut, valid_name).value = 0
        except Exception:
            pass
    await ClockCycles(dut.clk, 3)
    dut.rst.value = 0
    await RisingEdge(dut.clk)  # rst deassert sampled, outputs stable


def check(dut, expected, signal, test_name, cycle, fail_counter=None):
    """Raise AssertionError on mismatch. signal is a dut handle."""
    actual = int(signal.value)
    if expected != actual:
        if fail_counter:
            fail_counter.inc()
        width = len(signal)
        msg = (
            f"[FAIL] {test_name} cycle={cycle} {signal._name}: "
            f"expected=0x{expected:0{(width + 3) // 4}x} got=0x{actual:0{(width + 3) // 4}x}"
        )
        dut._log.error(msg)
        raise AssertionError(msg)


async def drive_inputs(dut, input_values, valid_name='valid_in',
                       ready_name='ready_out'):
    """Wait for ready, then drive valid=1 with input values.

    Timing (POST-NBA):
    1. Wait for ready=1
    2. Drive ALL inputs simultaneously (valid + data)
    3. Wait ONE RisingEdge — DUT samples inputs at this posedge, NBA fires
    4. Deassert inputs (for single_cycle) or hold (for hold_until_ack)

    After this function returns, the DUT has consumed the inputs and its
    registered state reflects the transition (e.g., loaded data, FSM advance).
    The caller should account for this consumed cycle when aligning the
    golden model trace for comparison (see DRIVE_PHASE_CYCLES).

    IMPORTANT limitations:
      - Assumes a simple valid/ready handshake (ready HIGH before valid asserted).
      - Does NOT handle pipeline handshake (ready may drop after valid asserted).
      - Does NOT handle backpressure (ready cycling low during processing).
      - For backpressure designs, write a custom drive function that polls
        ready continuously and holds valid high until the DUT accepts.

    Args:
        dut: cocotb DUT handle
        input_values: dict mapping port names to integer values
        valid_name: name of the valid input port
        ready_name: name of the ready/ack output port
    """
    # Wait for ready
    timeout = TIMEOUT_CYCLES
    ready_sig = getattr(dut, ready_name)
    while int(ready_sig.value) != 1 and timeout > 0:
        await RisingEdge(dut.clk)
        timeout -= 1
    assert timeout > 0, "Timeout waiting for ready before drive_inputs"

    # Drive all inputs at once
    for port_name, value in input_values.items():
        getattr(dut, port_name).value = value
    getattr(dut, valid_name).value = 1

    # Wait one rising edge — DUT samples inputs at this posedge
    await RisingEdge(dut.clk)

    # Deassert based on handshake protocol
    if valid_name in HOLD_UNTIL_ACK_PORTS:
        # hold_until_ack: keep valid high until DUT asserts ready
        # (ready was already high when we started, so DUT has sampled.
        #  But for multi-cycle holds, wait for ready to be asserted again)
        pass  # caller is responsible for deasserting after ack
    else:
        # single_cycle / pulse: deassert after DUT has sampled
        getattr(dut, valid_name).value = 0
        for port_name in input_values:
            getattr(dut, port_name).value = 0


async def wait_valid(dut, valid_name='valid_out', max_cycles=None):
    """Poll for valid=1, return cycle count waited."""
    if max_cycles is None:
        max_cycles = TIMEOUT_CYCLES
    valid_sig = getattr(dut, valid_name)
    for i in range(max_cycles):
        await RisingEdge(dut.clk)
        if int(valid_sig.value) == 1:
            return i + 1
    return -1  # timeout


# ─── Multi-block NBA-aware wait pattern ─────────────────────────────────
# IMPORTANT: When sending multiple blocks to a DUT with FSM (IDLE→CALC→DONE→IDLE),
# you MUST account for NBA propagation after driving each block.
#
# At RisingEdge N, cocotb reads POST-NBA of posedge N-1.
# After drive_inputs(), the DUT transitions state at the NEXT posedge.
# You must wait at least ONE RisingEdge for the NBA to propagate before
# polling ready/state signals.
#
# WRONG (reads stale ready=1 from previous IDLE state):
#   drive_inputs(dut, ...)
#   while int(dut.ready.value) != 1:   # stale! DUT still in IDLE at this point
#       await RisingEdge(dut.clk)
#
# CORRECT:
#   drive_inputs(dut, ...)
#   await RisingEdge(dut.clk)          # let NBA propagate: IDLE → CALC
#   while int(dut.ready.value) != 1:   # now reads post-NBA CALC state
#       await RisingEdge(dut.clk)
#
# This pattern applies to ANY multi-block/multi-operation test where the DUT
# goes through state transitions between operations.


