#!/usr/bin/env python3
"""Wave 3 helper: extract constants, inject S8, merge parts, validate.

Usage: python merge_design_spec.py <project_dir>

Eliminates all shell quoting/escaping issues by doing everything in Python.
"""

import importlib.util
import inspect
import os
import re
import sys
from pathlib import Path


def extract_constants(parts_dir: Path, output_path: Path) -> None:
    """Step 1: Execute all zero-arg print_*_verilog() functions and save output."""
    s3_path = parts_dir / "s3_constants.py"
    if not s3_path.exists():
        print("[WARN] s3_constants.py not found, skipping constant extraction")
        return

    spec = importlib.util.spec_from_file_location("s3_constants", str(s3_path))
    mod = importlib.util.module_from_spec(spec)
    sys.path.insert(0, str(parts_dir))
    spec.loader.exec_module(mod)

    with open(output_path, "w", encoding="utf-8") as f:
        for name, obj in inspect.getmembers(mod, inspect.isfunction):
            if name.startswith("print_") and name.endswith("_verilog"):
                sig = inspect.signature(obj)
                try:
                    if len(sig.parameters) == 0:
                        result = obj()
                    else:
                        # Skip generic helpers that need arguments
                        continue
                except Exception as e:
                    print(f"[WARN] {name}() failed: {e}")
                    continue
                f.write(f"// === Extracted from design_spec.py: {name}() ===\n")
                f.write(result)
                f.write("\n\n")

    print("[OK] Constant extraction complete")


