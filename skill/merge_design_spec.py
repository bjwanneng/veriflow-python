def strip_future_imports(content: str) -> str:
    """Remove 'from __future__ import ...' lines from content."""
    return re.sub(r'^from __future__ import .*\n?', '', content, flags=re.MULTILINE)


def strip_cross_module_imports(content: str) -> str:
    """Remove 'from workspace.docs._parts import ...' lines from content."""
    return re.sub(r'^from workspace\.docs\._parts import .*\n?', '', content, flags=re.MULTILINE)


def merge(parts_dir: Path, output_path: Path) -> None:
    """Step 3: Merge all parts into final design_spec.py."""
    header = '"""design_spec.py -- Python Design Specification.\n\nGenerated by VeriFlow Stage 1 (optimized).\n"""\n\n'
    parts_order = [
        "s1_interface", "s2_hierarchy", "s3_constants",
        "s4_helpers", "s5_pseudocode", "s7_test_vectors",
    ]

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(header)

        # Sections 1-5 and 7, with cleanup
        for part in parts_order:
            path = parts_dir / (part + ".py")
            if not path.is_file():
                print(f"[FATAL] Missing part: {path}")
                sys.exit(1)
            content = path.read_text(encoding="utf-8")
            # Strip problematic imports that don't work in merged file
            content = strip_future_imports(content)
            content = strip_cross_module_imports(content)
            f.write(content)
            f.write("\n\n")

        # Section 6: compute() with scaffold wrapper + agent body
        compute_path = parts_dir / "s6_compute_body.py"
        if not compute_path.is_file():
            print(f"[FATAL] Missing part: {compute_path}")
            sys.exit(1)

        compute_body = compute_path.read_text(encoding="utf-8")
        compute_body = strip_future_imports(compute_body)
        compute_body = strip_cross_module_imports(compute_body)

        f.write("# ============================================================\n")
        f.write("# Section 6: Top-Level Integration\n")
        f.write("# ============================================================\n\n")
        f.write("def compute(inputs: dict, trace: bool = False) -> dict | list[dict]:\n")
        f.write('    """Execute the design cycle-accurately.\n\n')
        f.write('    Args:\n')
        f.write('        inputs: {"blocks": [int, ...], "is_last_flags": [bool, ...]}\n')
        f.write('        trace:  False -> dict of final outputs\n')
        f.write('                True  -> list[dict] per cycle for cocotb comparison\n')
        f.write('    """\n')
        for line in compute_body.splitlines():
            if line.strip():
                f.write("    " + line + "\n")
            else:
                f.write("\n")
        f.write("\n\n")

        # Section 8: auto-injected scaffold
        s8_path = parts_dir / "s8_interface.py"
        if not s8_path.is_file():
            print(f"[FATAL] Missing part: {s8_path}")
            sys.exit(1)
        f.write(s8_path.read_text(encoding="utf-8"))

    print(f"[OK] Merged into {output_path}")


def main():
    if len(sys.argv) < 2:
        print("Usage: python merge_design_spec.py <project_dir>", file=sys.stderr)
        sys.exit(1)

    project_dir = Path(sys.argv[1]).resolve()
    parts_dir = project_dir / "workspace" / "docs" / "_parts"
    output_path = project_dir / "workspace" / "docs" / "design_spec.py"
    constants_path = project_dir / "workspace" / "docs" / "_extracted_constants.v"

    print(f"[INFO] Project: {project_dir}")
    print(f"[INFO] Parts:   {parts_dir}")

    extract_constants(parts_dir, constants_path)
    inject_s8(parts_dir)
    merge(parts_dir, output_path)

    print("[OK] Wave 3 complete")


if __name__ == "__main__":
    main()
